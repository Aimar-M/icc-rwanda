'use client'

import { useEffect, useState, useRef } from 'react'
import { motion, useInView } from 'framer-motion'

const stats = [
  { value: 100, suffix: '+', label: 'Women Empowered', icon: WomenIcon },
  { value: 120, suffix: '+', label: 'Students Supported', icon: StudentsIcon },
  { value: 500, suffix: '+', label: 'Healthcare / Mutuelle', icon: HealthcareIcon },
  { value: 100, suffix: '+', label: 'Supporting Small Businesses with Capital', icon: BusinessIcon },
]

function AnimatedCounter({ value, suffix, isInView }: { value: number; suffix: string; isInView: boolean }) {
  const [count, setCount] = useState(0)

  useEffect(() => {
    if (!isInView) return

    const duration = 2000
    const steps = 50
    const increment = value / steps
    const stepDuration = duration / steps

    let current = 0
    const timer = setInterval(() => {
      current += increment
      if (current >= value) {
        setCount(value)
        clearInterval(timer)
      } else {
        setCount(Math.floor(current))
      }
    }, stepDuration)

    return () => clearInterval(timer)
  }, [isInView, value])

  return (
    <span className="font-display text-4xl md:text-5xl font-bold text-white">
      {count}{suffix}
    </span>
  )
}

export default function ImpactStats() {
  const ref = useRef(null)
  const isInView = useInView(ref, { once: true, margin: '-100px' })

  return (
    <section ref={ref} className="bg-gradient-hero py-16 md:py-20">
      <div className="container-custom">
        <div className="grid grid-cols-2 lg:grid-cols-4 gap-8 md:gap-12">
          {stats.map((stat, index) => (
            <motion.div
              key={stat.label}
              initial={{ opacity: 0, y: 20 }}
              animate={isInView ? { opacity: 1, y: 0 } : {}}
              transition={{ duration: 0.5, delay: index * 0.1 }}
              className="text-center"
            >
              <div className="w-16 h-16 mx-auto mb-4 rounded-full bg-white/10 flex items-center justify-center">
                <stat.icon className="w-8 h-8 text-accent" />
              </div>
              <AnimatedCounter value={stat.value} suffix={stat.suffix} isInView={isInView} />
              <p className="mt-2 text-white/80 font-medium">{stat.label}</p>
            </motion.div>
          ))}
        </div>
      </div>
    </section>
  )
}

